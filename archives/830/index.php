<h1 class="entry-title">
											染め直しサービス　”藍染を使った服の染め直しサービスで、お洒落と思い出を再び”									</h1>
</header>
<div class="entry-body">
<p>私たちはシミや色褪せてしまって着なくなってしまった洋服をお客様から依頼を受けて染め直し、また着れる洋服へと生まれ変わらせる<strong>「染め直しサービス」</strong>を行っています。</p>
<p>そこで今回お客様から染め直しの依頼を受けたのでご紹介させていただきます。</p>
<h2>カウンセリング内容</h2>
<p>今回の依頼は、長年来ていたシャツが経年劣化により色褪せてきてしまったが、友人からのプレゼントで捨てることができない。そこで色褪せが気にならないように藍染めで染めてもらいたい、とのことでした。</p>
