<h3>デザインのポイント</h3>
<p>お客様とのカウンセリングをもとに今回の染め直しを行いました。</p>
<p>デザインをつける箇所とデザインのつけない箇所を左右や上下で分けるのではなく、シンメトリーに分けることでおしゃれさやこなれ感を演出することができます。</p>
<p>そしてデザインをつけた部分は<strong>叢雲絞り</strong>という技法を用いて染め、不規則な模様を演出し派手すぎる印象を与えないようにしています。</p>
<p>さらに夏に爽やかさを感じたいというご要望には、染料の濃度を薄めに調節し夏に爽やかさが映える淡い水色のような色味に仕上げています。</p>
<p> </p>
<div class="box-019">
<div>お客様の声</div>
<p>カウンセリングで自分の要望を細かく伝えることができイメージの共有を徹底して行えることにまず安心感が持てました。<br/>
また、自分の好みや要望をできるだけ汲み取っていたき、その上で可能なこと、難しいことをちゃんと伝えてもらえたこともとてもありがたかったです。</p>
<p>仕上がりに関しましては期待以上の仕上がりでこのシャツを着るのが毎回楽しみになっています！デザインや色味も自分好みに仕上がりとても嬉しいです。</p>
<p>こういった形で服が生まれ変わるとまた思い入れが強まり大事にすることができます。</p>
<p>さらに、来ていくことで藍の色味の変化も楽しむことができるそうで、楽しみが続いていくこともとても嬉しいです。</p>
<p>本当にありがとうございました。</p>
